$(function () {

  /*
  * Раздельные галереи на одной странице.  
  */
  $('.gallery').each(function () {
    $(this).magnificPopup({
      delegate: 'a',
      type: 'image',
      gallery: {
        enabled: true
      }
    });
  });

});/* $(function(){}) */